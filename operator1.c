#include<stdio.h>
int main()
{
	
	int no = 11;

	char ch = 'A';


	

	printf("value of ch : %c \n",ch);

	printf("value of no : %d \n",no);

	printf("address of no : %u\n",no);

	printf("address of ch : %u\n",ch);

	printf("size of no : %d\n",sizeof (no));

	printf("size of ch : %d\n",sizeof (ch));

	return 0;

}

